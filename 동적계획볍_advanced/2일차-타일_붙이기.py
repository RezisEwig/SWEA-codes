T = int(input())
# 여러개의 테스트 케이스가 주어지므로, 각각을 처리합니다.
for test_case in range(1, T + 1):
    # ///////////////////////////////////////////////////////////////////////////////////
    N = int(input())
    mylist = [1, 3, 6]
    for i in range(3, N) :
        mylist.append(mylist[i-3] + 2*mylist[i-2] + mylist[i-1])
        
    print("#{0} {1}".format(test_case, mylist[N-1]))
    # ///////////////////////////////////////////////////////////////////////////////////